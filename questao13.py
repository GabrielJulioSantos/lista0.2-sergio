n=int(input('digite um numero:'))
if n==1:
    print('dia {} é domingo'.format(n))
if n==2:
    print('dia {} é segunda'.format(n))
if n==3:
    print('dia {} é terça'.format(n))
if n==4:
    print('dia {} é quarta'.format(n))
if n==5:
    print('dia {} é quinta'.format(n))
if n==6:
    print('dia {} é sexta'.format(n))
if n==7:
    print('dia {} é sabado'.format(n))
else:
    print('numero invalido')